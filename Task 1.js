let age = 18;
if (age >= 18) {
    console.log("You are eligible to vote.");
}


let number = 10;
if (number % 2 === 0) {
    console.log("Even number");
} else {
    console.log("Odd number");
}


let marks = 75;
if (marks >= 90) {
    console.log("Grade A");
} else if (marks >= 70) {
    console.log("Grade B");
} else {
    console.log("Grade C");
}


let day = 3;
switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    default:
        console.log("Invalid day");
}
